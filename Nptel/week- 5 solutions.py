# using basic method
n = int(input())
square = {}

for i in range(1, n+1):
    square[i] = i * i


print(square, end="")

# using Dictionary Comprehension
n = int(input())
square_dict = {num: num*num for num in range(1, n+1)}
print(square_dict, end="")

